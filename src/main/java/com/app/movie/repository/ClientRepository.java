package com.app.movie.repository;

import com.app.movie.entities.Client;
import com.app.movie.interfaces.IClientRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Repository;

import java.util.List;
import java.util.Optional;

@Repository
public class ClientRepository {
    
    @Autowired
    IClientRepository repository;
    
    public Iterable<Client> getAll(){
        return repository.findAll();
    }
    
    public Optional<Client> findById(String id){
        Optional<Client> response= repository.findById(id);
        return response;
    }
    public Optional<Client> findByEmail(String email){
        List<Client> response= repository.findByEmail(email);
        if(response.size()>0){
            return Optional.ofNullable(response.get(0));

        }
        else {
            Optional<Client> client = Optional.of(new Client());
            return client;
        }

    }
    
    public Boolean existsById(String id){
        return repository.existsById(id);
    }
    
    public void deleteById(String id){
        repository.deleteById(id);
    }
    
    public Client save(Client client){
        return repository.save(client);
    }
    
}
